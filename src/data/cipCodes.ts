// Search CIP codes by title or code
export const searchCIPCodes = (query: string): CIPCode[] => {
  const searchQuery = query.toLowerCase().trim();
  if (!searchQuery || searchQuery.length < 2) return [];

  return CIP_CODES.filter(
    (cip) =>
      cip.code.includes(searchQuery) ||
      cip.title.toLowerCase().includes(searchQuery) ||
      cip.category.toLowerCase().includes(searchQuery)
  ).slice(0, 20);
};

// Get CIP code by exact code
export const getCIPByCode = (code: string): CIPCode | undefined => {
  return CIP_CODES.find((cip) => cip.code === code);
};

// Get CIP codes by category
export const getCIPByCategory = (category: string): CIPCode[] => {
  return CIP_CODES.filter((cip) => cip.category === category);
};

