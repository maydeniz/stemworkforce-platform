// Helper function to get event by ID or slug
export function getSampleEventByIdOrSlug(idOrSlug: string): Event | undefined {
  return sampleEvents.find(
    (event) => event.id === idOrSlug || event.slug === idOrSlug
  );
}

// Helper function to get related events
export function getRelatedSampleEvents(eventId: string, limit = 4): Event[] {
  const event = sampleEvents.find((e) => e.id === eventId);
  if (!event) return [];

  // Find events with matching industries or category
  return sampleEvents
    .filter((e) => e.id !== eventId)
    .filter(
      (e) =>
        e.category === event.category ||
        e.industries.some((ind) => event.industries.includes(ind))
    )
    .slice(0, limit);
}

